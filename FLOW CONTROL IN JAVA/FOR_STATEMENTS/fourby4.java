
public class fourby4 {

    public static void main(String[] args) {

        int i = 1;
        int j = 5;

        for (int start1 = 1; start1 <= i; start1++) {

            for (int start2 = 1; start2 <= j; start2++) {
                System.out.print("*");
            }
            System.out.println("");

        }

    }
}
